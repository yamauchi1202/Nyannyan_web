package jp.co.nyannyan.form;

public class UserCouponForm {

	private Integer userCoupon_id;
	private Integer user_id;
	private Integer coupon_id;
	private Integer coupon_flag;

	public Integer getUserCoupon_id() {
		return userCoupon_id;
	}
	public void setUserCoupon_id(Integer userCoupon_id) {
		this.userCoupon_id = userCoupon_id;
	}
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	public Integer getCoupon_id() {
		return coupon_id;
	}
	public void setCoupon_id(Integer coupon_id) {
		this.coupon_id = coupon_id;
	}
	public Integer getCoupon_flag() {
		return coupon_flag;
	}
	public void setCoupon_flag(Integer coupon_flag) {
		this.coupon_flag = coupon_flag;
	}

}
